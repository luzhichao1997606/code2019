#include "variable.h"

/********************************************************************************
*																		校验函数																		*
********************************************************************************/
UINT16 Check_Sum(UINT8 *Src,
                 UINT8 Length    //数组长度
                )
{
  UINT16 crc = *++Src;

  while (--Length)
  {
    crc ^= *++Src;
  }

  return crc;
}

